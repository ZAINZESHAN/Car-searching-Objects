var company = document.getElementById("company");
var model = document.getElementById("model");
var specification = document.getElementById("specification");

for (var key in car) {
    company.innerHTML += `<option value='${key}'> ${key} </option>`
}

function selectCompany() {

    model.innerHTML = `<option> Select Model </option>`

    for (var key in car[company.value]) {
        model.innerHTML += `<option value='${key}'> ${key} </option>`
    }
}

function selectModel() {

    var selectCompany = company.value;
    var selectModel = model.value;

    if (model.value) {
        var details = car[selectCompany][selectModel]

        specification.innerHTML =

            ` <div class="slide d-flex">
                <div class="box-1">
                    <h2 class="carname">${details.carName} <span>(${details.model})</span></h2>
                    ${details.image}
                </div>
                <div class="box-2">
                    <h2>Details</h2>
                    <ul>
                        <li><strong>Model:</strong> ${details.model}</li>
                        <li><strong>Fuel Type:</strong> ${details.fuelType}</li>
                        <li><strong>Engine:</strong> ${details.engine}</li>
                        <li><strong>Seat Capacity:</strong> ${details.seatCapacity}</li>
                        <li><strong>Fuel Tank Capacity:</strong> ${details.fuelTankCapacity}</li>
                    </ul>
                </div>
            </div>`


    }
    else {
        specification.innerHTML = "";
    }
}

